package Nhom4.Dto;

import java.util.Date;

public class Change {
	Date from;
	Date to;
	String option;
	Date from2;
	Date to2;
	public Change() {
		super();
	}
	public Date getFrom() {
		return from;
	}
	public void setFrom(Date from) {
		this.from = from;
	}
	public Date getTo() {
		return to;
	}
	public void setTo(Date to) {
		this.to = to;
	}
	public String getOption() {
		return option;
	}
	public void setOption(String option) {
		this.option = option;
	}
	public Date getFrom2() {
		return from2;
	}
	public void setFrom2(Date from2) {
		this.from2 = from2;
	}
	public Date getTo2() {
		return to2;
	}
	public void setTo2(Date to2) {
		this.to2 = to2;
	}
	
	
}
